  async encodeMessage(data) {
    return encode(data);
  }

  async decodeMessage(data) {
    // Browsers provide Blob while node gives straight ArrayBuffer
    if (typeof Blob !== 'undefined' && data instanceof Blob) {
      data = await data.arrayBuffer();
    }

    return decode(data);
  }

